# SIDE NOTES
# ***********************************************************
# dictionaries are python's implementation of HashTables
# dictionaries can contain any types of data (keys or values)
# keys must be immutable
dict = {'Name': 'Zara', 'Age': 7, 'Class': 'First'}


# OPERATIONS
# ***********************************************************
dict['Age']; # ACCESS

dict['School'] = "DPS School" # ADD NEW ENTRY

dict['Age'] = 8 # UPDATE ENTRY

del dict['Age'] # REMOVE ENTRY

len(dict) # LENGTH

str(dict) # PRINTS ALL ELEMENTS

dict.clear() # CLEARS DICT

dict.values() # RETURNS LIST OF ALL VALUES

dict.keys() # RETURNS LIST OF ALL KEYS


