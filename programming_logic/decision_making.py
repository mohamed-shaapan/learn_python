# SIMPLE IF
# ***********************************************************
var1 = 100
if var1:
   print ("1 - Got a true expression value")
   print (var1)

# IF ELSE
# ***********************************************************
var1 = 100
if var1:
   print ("1 - Got a true expression value")
   print (var1)
else:
   print ("1 - Got a false expression value")
   print (var1)

# ELSE IF
# ***********************************************************
var = 100
if var == 200:
   print ("1 - Got a true expression value")
   print (var)
elif var == 150:
   print ("2 - Got a true expression value")
   print (var)
else:
   print ("4 - Got a false expression value")
   print (var)

